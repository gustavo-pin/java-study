public class Variables {
    /*
    * boolean - 1bit   - primitive - true or false
    * byte    - 1byte  - primitive - -128 to 127
    * short   - 2bytes - primitive - -32,768 to 32,767
    * int     - 4bytes - primitive - -2billion to 2billion
    * long    - 8bytes - primitive - -9quintillion to 9quintillion
    *
    * float   - 4bytes - primitive - 6-7 fractional decimal houses
    * double  - 8bytes - primitive - up to 15 fractional decimal houses
    *
    * char    - 2bytes - primitive - single ASCII value
    * String  - varies - reference - a sequence of characters
    * */

    public boolean myBoolean = true;
    public byte   myByte     = 127;
    public short  myShort    = 32767;
    public int    myInt      = 2000000000;
    public long   myLong     = 6496515646316436162L;

    public float  myFloat    = 0.34f;
    public double myDouble   = 0.046564665564564654545445;

    public char   myChar     = 'c';
    public String mystring   = "Hello world";
}